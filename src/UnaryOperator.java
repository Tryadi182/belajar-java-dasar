public class UnaryOperator {
    public static void main(String[] args) {

        /**
        // Unary Operator
        // ditempatkan dibelakang variabel

        //Operator	Keterangan	        Contoh
        //  ++	    Increment	        a = a + 1;
        //  --	    Decrement	        a = a - 1;
        //   -	    Unary Minus	        int b = -5;
        //   +	    Unary Plus	        int c = +5;
        //   !	    Logical Complement	boolean d = true;
        //   ~	    Bitwise Complement	int e = ~5;
         */

        int d = 100;
        int e = ~5;

        d++;
        //sama dengan d = d + 1 (101)
        System.out.println(d);

        d--;
        //sama dengan d = d - 1 (101 (101 dari operasi penjumlahan diatas sebelumnya) - 1 = 100)
        System.out.println(d);

        System.out.println(e); //belum bisa menjelaskan wkwk, coba takono mas bay

    }
}
