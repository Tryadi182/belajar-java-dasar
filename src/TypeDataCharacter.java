public class TypeDataCharacter {
    public static void main(String[] args) {

        /**
        //Tipe Data Character
        //Data Character (huruf) di Java direpresentasikan oleh tipe char.
        //Untuk membuat data char di Java, kita bisa menggunakan tanda' (petik satu)
        // di awal dan di akhir karakter*/

        char t = 'T';
        char r = 'R';
        char y = 'Y';

        //untuk penambahan spasi bawah perintahnya System.out.println();
        System.out.println(t);
        System.out.println(r);
        System.out.println(y);

        //untuk penambahan spasi biasa perintahnya System.out.print();
        System.out.print(t);
        System.out.print(r);
        System.out.print(y);
    }
}
